bool is_mod_tab_active = false;
uint16_t mod_tab_timer = 0;

void matrix_scan_user(void) {
  if (is_mod_tab_active) {
    if (timer_elapsed(mod_tab_timer) > 750) {
      unregister_code(KC_LGUI);
      is_mod_tab_active = false;
    }
  }
}

